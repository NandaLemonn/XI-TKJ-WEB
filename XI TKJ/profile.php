        <b>4. Penelusuran dan Pemasaran Tamatan Siswa</b>
        <p>Sejak tahun 2012 SMK Kristen 5 Klaten menjadi afiliasi BKK baik di wilayah eks Karesidenan Surakarta, maupun wilayah Yogyakarta yang dihimpun oleh SMK Negeri 2 Depok Yogyakarta.
         Sehingga terbuka kesempatan untuk mengadakan MoU dengan perusahaan-perusahaan tingkat internasional di kawasan Jakarta, Cikarang, Kerawang, Bekasi, Tangerang, bahkan pemagangan ke Jepang. Dengan prestasi baik SMK Kristen 5 Klaten dipercaya beberapa perusahaan menjadi basis perekrutan (seleksi) masuk kerja, diantaranya :
