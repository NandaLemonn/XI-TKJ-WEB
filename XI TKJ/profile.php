        <h2>PROFILE SMK KRISTEN 5 KLATEN</h2>
        <p>Pada awalnya SMK Kristen 5 Klaten didirikan dengan nama STM Kristen 2 Klaten. Didirikan dengan SK Yayasan Pendidikan Kristen Klaten (YPKK) Nomor : 123.B/B.10/V/92, tertanggal 29 Mei 1992.
            Untuk pertama kali dibuka dengan jurusan :</p>
            <ul>
                <li>Mekanik Otomotif</li>
                <li>Elektronika Komunikasi</li>
            </ul>
        <p>Sedang tempat kegiatan belajar mengajar masih meminjam gedung STM Kristen 1 Klaten, masuk siang. Pada tahun berikutnya dengan bertambahnya murid, tempat belajar mengajar bertambah menempati Gedung eks PGAK Klaten, Jl Dewi Sartika.   
        Sedang pada tahun 1995/1996 semua kegiatan belajar mengajar menempati gedung eks SMA Kristen 1 Klaten, yang berlokasi</p>
        <p>Atas berkat dan penyertaan Tuhan, dibangunlah ruang praktik Otomotif dan selesai pada awal
         Nopember 1997. Karena itu, terhitung mulai tanggal 10 Nopember 1997 semua kegiatan belajar mengajar (termasuk praktik) berada di satu tempat : STM Kristen 2 Klaten, Jl Opak, Methuk kidul, Tegalyoso, Klaten Selatan.</p>
         <b>Perkembangan Sekolah</b>
         <br>
         <b>A. Periode kepemimpinan Drs. Sugeng Prasetyo (1992-2009)</b>
         <p>Sejak didirikan tahun 1992 dengan Kepala Sekolah Sdr Drs. Sugeng Prasetyo, sekolah berkembang cukup bagus. Sampai dengan tahun 2005/2006,
         dengan 2 jurusan rata-rata menerima murid baru 200 orang. Dengan rincian 120 orang jurusan Otomotif, dan 80 orang jurusan Elektronika Komunikasi.</p>
         <p>Sejak tahun 2006/2007, atas izin Kepala Dinas Pendidikan dan Kebudayaan Kab. 
            Klaten dengan SK No 421.5/991/13 tertanggal 16 April 2006, dibukalah 2 program baru, yaitu :
        </p>
        <ul>
            <li>
            Program Body Otomotif (BO); sebuah program keahlian yang belum ada di Klaten waktu itu.
            Bahkan se-karesidenan Surakarta baru ada di SMK N 5 Surakarta, dan SMK Simo, Boyolali. Sampai sekarang animo untuk prodi BO masih bagus.
            </li>
            <li>
            Program Body Otomotif (BO); sebuah program keahlian yang belum ada di Klaten waktu itu.
            Bahkan se-karesidenan Surakarta baru ada di SMK N 5 Surakarta, dan SMK Simo, Boyolali. Sampai sekarang animo untuk prodi BO masih bagus.
            </li>
        </ul>
        <p>Selanjutnya berdasarkan Surat Edaran Sekjen Depdikbud No 410007/A.A5/OT/1997, perihal tindak lanjut keputusan Menteri Pendidikan dan Kebudayaan RI,
         No 034.035 dan 036/O/1997, tentang perubahan nomenklatur sekolah, maka pada tahun 1997 STM Kristen 2 Klaten, berubah nama menjadi SMK Kristen 5 Klaten.
        </p>
        <p>Dari sisi fisik sekolah, SMK Kristen 5 Klaten menempati gedung eks SMA Kristen 1 Klaten,
         dengan jumlah ruang kelas sangat terbatas. Karena itu sekolah terus menambah fasilitas/ruangan untuk belajar, praktik, atau yang lain.</p>
         <p>Oktober 1995, telah selesai membangun 2 ruang kelas, 1 ruang praktik elektronika dan ruang Perpustakaan.
         Proses pembangunan ini dibantu oleh beberapa sekolah di lingkungan YPKK. Oktober 1997, selesai membangun ruang praktik Otomotif dan tempat sepeda. 17 Juli 2002, membeli tanah sawah 2.365 m2, sebelah utara gedung sekolah.</p>
         <p>Atas berkat dan penyertaan Tuhan, tahun 2004 sekolah mendapatkan bantuan dari Educan Belanda. Di atas tanah sawah itu, dibangun ruang praktik Otomotif, ruang Tehnik Komputer dan Jaringan, ruang Guru, 
            dan ruang parkir sepeda motor serta bantuan pembelian peralatan untuk praktik Otomotif dan Komputer. Peningkatan kompetensi pendidik, dengan mengikutkan pelatihan VEDC di Malang dan PPPG Bandung.</p>
        <p>Desember 2008, membeli sebidang tanah 492 m2, sebelah utara gedung sekolah. (yang kelak didirikan laboratorium/bengkel Tehnik Sepeda Motor).</p>
        <br>
        <b>B. Periode kepemimpinan Drs. Putut Kustriatmo, M.Pd (2009-sekarang).</b>
        <br>
        <b>1. Peningkatan Sumber Daya Pendidik</b>
        <p>Mengirimkan guru-guru ke LPMP, Dikjur, P4TK, untuk meningkatkan kualitas (kompetensi) guru hingga mampu menjadi Instruktur, baik tingkat Kabupaten,
         Propinsi, maupun Nasional. Memagangkan guru ke Dunia Usaha dan Dunia Industri yang sesuai bidangnya. Mengirim Kepala sekolah untuk mengikuti program studi banding ke sekolah luar negeri.</p>
         <br>
         <b>2. Dari Sisi Pengembangan Program Keahlian</b>
         <p>Sekolah membuka program keahlian baru Tehnik Sepeda Motor, dengan SK Kepala Dinas Pendidikan Kabupaten Klaten No. 421.5/975/11, tanggal 09 Maret 2012.
             Kemudian pada Februari 2014 diresmikan oleh Bupati Klaten, Sunarno, SE, MM sebagai kelas industri di bawah manajemen Astra Honda Motor. Dengan demikian SMK Kristen 5 memiliki lima program keahlian, dan tiga di antaranya telah memperoleh Sertifikat Tempat Uji Kompetensi (TUK), yaitu :
        </p>
        <ul>
            <li>
                Teknik Kendaraan Ringan Otomotif 
            </li>
            <li>
                Teknik dan Bisnis Sepeda Motor  
            </li>
        </ul>
